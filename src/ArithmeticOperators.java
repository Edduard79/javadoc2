/**
 * @author eduard
 * This class makes arithmetics opreations
 */
public class ArithmeticOperators {
    public ArithmeticOperators(){}

    /**
     *
     * @param num1 first number assigned
     * @param num2 second number assigned
     * @return the sum of num1 and num2
     */
    public double sum(double num1, double num2){
        return num1 + num2;
    }

    /**
     *
     * @param num1 first number assigned
     * @param num2 second number assigned
     * @return the subtraction of num1 minus num2
     */
    public double sub(double num1, double num2){
        return num1 - num2;
    }

    /**
     *
     * @param num1 first number assigned
     * @param num2 second number assigned
     * @return the product of num1 multiplied by num2
     */
    public double mul(double num1, double num2){
        return num1 * num2;
    }

    /**
     *
     * @param num1 first number assigned
     * @param num2 second number assigned
     * @return the quotient of num1 divided by num2
     */
    public double div(double num1, double num2){
        return num1 / num2;
    }



}
